    public void resetTable(String keyword) {
        try {
            java.sql.Connection conn = new koneksi.Koneksi().getConnection();
            javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
            model.setColumnIdentifiers(new String[]{"ID Perawat", "Nama Perawat", "Jenis Kelamin", "No Telp", "Alamat"});

            String sql = "SELECT * FROM perawat" + keyword;
            java.sql.Statement st = conn.createStatement();
            java.sql.ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_perawat"),
                    rs.getString("nama_perawat"),
                    rs.getString("jenis_kelamin"),
                    rs.getString("no_telp"),
                    rs.getString("alamat")
                });
            }
            tabelPerawat.setModel(model);
            conn.close();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Gagal menampilkan data: " + e.getMessage());
        }
    }

    public void setListener() {
        btnCari.addActionListener((e) -> {
            resetTable(" WHERE nama_perawat LIKE '%" + txtCari.getText() + "%'");
        });

        btnBatal.addActionListener((e) -> {
            txtCari.setText("");
            resetTable("");
        });

        btnTutup.addActionListener((e) -> {
            dispose();
        });

        btnHapus.addActionListener((e) -> {
            int row = tabelPerawat.getSelectedRow();
            if (row == -1) {
                javax.swing.JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus!");
                return;
            }
            int id = Integer.parseInt(tabelPerawat.getValueAt(row, 0).toString());
            int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(null, "Yakin hapus data?", "Konfirmasi", javax.swing.JOptionPane.YES_NO_OPTION);
            if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
                try {
                    java.sql.Connection conn = new koneksi.Koneksi().getConnection();
                    java.sql.PreparedStatement pst = conn.prepareStatement("DELETE FROM perawat WHERE id_perawat = ?");
                    pst.setInt(1, id);
                    pst.executeUpdate();
                    javax.swing.JOptionPane.showMessageDialog(null, "Data dihapus!");
                    resetTable("");
                    conn.close();
                } catch (Exception ex) {
                    javax.swing.JOptionPane.showMessageDialog(null, "Gagal hapus: " + ex.getMessage());
                }
            }
        });

        btnTambah.addActionListener((e) -> {
            PerawatTambahFrame tambah = new PerawatTambahFrame();
            tambah.setVisible(true);
        });

        btnUbah.addActionListener((e) -> {
            int row = tabelPerawat.getSelectedRow();
            if (row == -1) {
                javax.swing.JOptionPane.showMessageDialog(null, "Pilih data yang ingin diubah!");
                return;
            }
            String id = String.valueOf(tabelPerawat.getValueAt(row, 0));
            String nama = String.valueOf(tabelPerawat.getValueAt(row, 1));
            String jk = String.valueOf(tabelPerawat.getValueAt(row, 2));
            String telp = String.valueOf(tabelPerawat.getValueAt(row, 3));
            String alamat = String.valueOf(tabelPerawat.getValueAt(row, 4));

            PerawatTambahFrame ubah = new PerawatTambahFrame(id, nama, jk, telp, alamat);
            ubah.setVisible(true);
        });
    }
